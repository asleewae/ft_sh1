#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*dest;

	if (src)
	{
		i = 0;
		if ((dest = malloc(sizeof(char) * ft_strlen(src) + 1)) == NULL)
			return (NULL);
		while (src[i])
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
		return (dest);
	}
	return (NULL);
}
